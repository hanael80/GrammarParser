////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief	정적으로 할당된 배열 내 항목의 수를 센다.
///
/// @param	array	정적으로 할당된 배열
////////////////////////////////////////////////////////////////////////////////////////////////////
#define HY_COUNT_OF( array ) (sizeof( array ) / sizeof( array[ 0 ] ))


////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief	주어진 조건이 만족되지 않으면 @a doWhenFail 을 실행한다.
///
/// @param	condition 	조건
/// @param	doWhenFail	조건이 만족되지 않았을 때 실행할 명령어
////////////////////////////////////////////////////////////////////////////////////////////////////
#define HY_ENSURE( condition, doWhenFail )                        \
if ( !(condition) )                                               \
{                                                                 \
	LOG( HyLog::Fatal, "Error occurred with '%s'.", #condition ); \
	doWhenFail;                                                   \
}
