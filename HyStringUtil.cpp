#include "PCH.h"
#include "HyStringUtil.h"
#include <algorithm>
#include <stdarg.h>


////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief	가변 인수로 전달된 형식화된 문자열로부터 최종 문자열을 만든다.
///
/// @param	str	형식화된 문자열
///
/// @return	최종 문자열
////////////////////////////////////////////////////////////////////////////////////////////////////
HyString HyStringUtil::Format( const HyChar* str, ... )
{
	HyChar  buf[ 1024 * 10 ];
	va_list args;
	va_start( args, str );
	vsprintf( buf, str, args );

	return buf;
}
